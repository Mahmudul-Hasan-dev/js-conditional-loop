var number = 0;

//loop
while (number <= 10) {

    //even number condition
    if (number % 2 == 0) {

        //print even number only the condition is true
        console.log(number);
    }
    number++;
}


//alternative to find even number
console.log('Alternative')
var number = 0;

//loop
while (number <= 10) {
    console.log(number);
    number = number + 2;
}